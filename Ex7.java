/*
關係運算子relational operator
1.boolean-->合-->true
		  -->不合-->false
2.>,<,>=,<=,==(等於),!=(不等於)
*/
class Ex7
{
	public static void main(String[] args) 
	{
		int chinese=80;
		int english=60;
		System.out.println("chinese="+chinese);
		System.out.println("english="+english);
		System.out.println("chinese > english--->"+(chinese>english));
		System.out.println("chinese < english--->"+(chinese<english));
		System.out.println("chinese >= english-->"+(chinese>=english));
		System.out.println("chinese <= english-->"+(chinese<=english));
		System.out.println("chinese == english-->"+(chinese==english));
		System.out.println("chinese != english-->"+(chinese!=english));
	}
}
